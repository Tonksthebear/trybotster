type IconGlyphProps = {
  name: string
  className?: string
}

/**
 * Render the SVG glyph for an icon name. Returns `null` when the name is not
 * in the registry so callers can decide whether to render a placeholder.
 */
export function IconGlyph({ name, className }: IconGlyphProps): ReactElement | null {
  const path = ICON_PATHS[name]
  if (path === undefined) return null
  return (
    <svg
      viewBox="0 0 20 20"
      fill="currentColor"
      aria-hidden="true"
      data-slot="icon"
      className={className}
    >
      <path d={path} />
    </svg>
  )
}

/** Returns true if the icon registry knows the given name. Exposed for tests. */
export function hasIcon(name: string): boolean {
  return Object.hasOwn(ICON_PATHS, name)
}
